package code_mar16;

public class Wrapper_Classes {
	
	public static void main(String[] args) {
		
		int a = 10;     // a is a variable of 'int' primitive data type which stores a value of 10
		Integer b = 20; // b is an object of integer data type which holds a value of 20
		
		System.out.println("Primitive Value: " + a);
		System.out.println("Wrapper Class Reference Variable (or Object): " + b);
	} 

}
